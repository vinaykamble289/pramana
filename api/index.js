async function setupVite(app2, server) {
  const serverOptions = {
    middlewareMode: true,
    hmr: { server },
    allowedHosts: true
  };
  if (vite_config_default.css?.postcss?.plugins) {
    const tailwindPlugin = vite_config_default.css.postcss.plugins.find((p) => p[0] === "tailwindcss");
    if (tailwindPlugin) {
      tailwindPlugin[1].content = [
        path2.resolve(import.meta.dirname, "..", "client", "index.html"),
        path2.resolve(import.meta.dirname, "..", "client", "src", "**", "*.{js,jsx,ts,tsx}")
      ];
    }
  }
  const vite = await createViteServer({
    ...vite_config_default,
    root: path2.resolve(import.meta.dirname, "..", "client"),
    resolve: {
      alias: {
        "@": path2.resolve(import.meta.dirname, "..", "client", "src"),
        "@shared": path2.resolve(import.meta.dirname, "..", "shared"),
        "@assets": path2.resolve(import.meta.dirname, "..", "attached_assets")
      }
    },
    configFile: false,
    customLogger: {
      ...viteLogger,
      error: (msg, options) => {
        viteLogger.error(msg, options);
        process.exit(1);
      }
    },
    server: serverOptions,
    appType: "custom"
  });
  app2.use(vite.middlewares);
  app2.use("*", async (req, res, next) => {
    const url = req.originalUrl;
    try {
      const clientTemplate = path2.resolve(
        import.meta.dirname,
        "..",
        "client",
        "index.html"
      );
      let template = await fs.promises.readFile(clientTemplate, "utf-8");
      template = template.replace(
        `src="/src/main.tsx"`,
        `src="/src/main.tsx?v=${nanoid()}"`
      );
      const page = await vite.transformIndexHtml(url, template);
      res.status(200).set({ "Content-Type": "text/html" }).end(page);
    } catch (e) {
      vite.ssrFixStacktrace(e);
      next(e);
    }
  });
}
function serveStatic(app2) {
  const distPath = path2.resolve(import.meta.dirname, "..", "public");
  if (!fs.existsSync(distPath)) {
    throw new Error(
      `Could not find the build directory: ${distPath}, make sure to build the client first`
    );
  }
  app2.use(express.static(distPath));
  app2.use("*", (_req, res) => {
    res.sendFile(path2.resolve(distPath, "index.html"));
  });
}

// server/index.ts
var app = express2();
app.use(express2.json());
app.use(express2.urlencoded({ extended: false }));
app.use((req, res, next) => {
  const start = Date.now();
  const path3 = req.path;
  let capturedJsonResponse = void 0;
  const originalResJson = res.json;
  res.json = function(bodyJson, ...args) {
    capturedJsonResponse = bodyJson;
    return originalResJson.apply(res, [bodyJson, ...args]);
  };
  res.on("finish", () => {
    const duration = Date.now() - start;
    if (path3.startsWith("/api")) {
      let logLine = `${req.method} ${path3} ${res.statusCode} in ${duration}ms`;
      if (capturedJsonResponse) {
        logLine += ` :: ${JSON.stringify(capturedJsonResponse)}`;
      }
      if (logLine.length > 80) {
        logLine = logLine.slice(0, 79) + "\u2026";
      }
      log(logLine);
    }
  });
  next();
});
registerRoutes(app);
app.use((err, _req, res, _next) => {
  const status = err.status || err.statusCode || 500;
  const message = err.message || "Internal Server Error";
  res.status(status).json({ message });
  throw err;
});
if (process.env.NODE_ENV === "development") {
  const server = createServer(app);
  (async () => {
    await setupVite(app, server);
    const port = parseInt(process.env.PORT || "5000", 10);
    server.listen(port, () => {
      console.log(`Server running at http://localhost:${port}`);
    });
  })();
} else {
  serveStatic(app);
}
var index_default = app;
export {
  index_default as default
};
